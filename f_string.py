# Look at Objects
class Car:
    def __init__(self, make, year):
        self.make = make
        self.year = year
    
    def __repr__(self) -> str:
        return f"{self.make}:{self.year}"

car_1 = Car("Ford", 2020)
car_2 = Car("Ferrari", 1991)
print(car_1)
print(car_2)

